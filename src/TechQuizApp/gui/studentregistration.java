private int validateinputs() {
        String user = uname.getText();
        char[] pwd = pass.getPassword();
        char[] pwd2 = repass.getPassword();
        if (user.isEmpty() || pwd.length == 0 || pwd2.length == 0) {
            return 0;
        } else if (user.length() <= 4 || pwd.length <= 4 || pwd2.length <= 4) {
            return -1;
        } else if (!Arrays.equals(pwd, pwd2)) {
            return -2;
        } else {
            return 1;
        }

    }

