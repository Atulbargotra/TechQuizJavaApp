    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int res = validateinputs();
        switch (res) {
            case -2:
                JOptionPane.showMessageDialog(null, "PASSWORDS does not match", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case -1:
                JOptionPane.showMessageDialog(null, "PASSWORD or USERNAME less than 4 character", "Error", JOptionPane.ERROR_MESSAGE);
                break;
            case 0:
                JOptionPane.showMessageDialog(null, "USERNAME or PASSWORD cannot be left blank", "Error", JOptionPane.ERROR_MESSAGE);
                break;
        }
        if (res == 1) {
            user = uname.getText().trim();
            password = pass.getText().trim();

            UserPojo obj = new UserPojo(user, password, utype);
            try {
                if (UserDao.adduser(obj)) {

                    JOptionPane.showMessageDialog(null, "USER sucessfully added to database", "SUCESS", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SQLException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(null, "Error while connecting to database", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

